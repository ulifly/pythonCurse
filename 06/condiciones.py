#if

"""
en python el uso de IF es muy similar a otros leguajes de programacion
se usa la palabra reservada if seguido de la condicion, s
i la condicion se cumple entonces se ejecuta el codigo dentro del if, 
si no se cumple entonces no se ejecuta nada. 
"""

edad = int(input("Ingrese su edad: "))
if edad >= 18:
    print("Usted es mayor de edad")
else:                                  #podemos tambien hacer uso de un else
    print("Usted es menor de edad")

"""
como podemos ver en el codigo anterior, si la condicion se cumple
entonces se ejecuta el codigo dentro del if, si no se cumple entonces
podemos hacer uso del else para ejecutar un codigo diferente.
"""