#en python una variable puede cambiar su tipo de datos sin ningun problema
#a esto se le conoce como tipado dinamico

valor = 'esto es un string'
print(valor)
print(type(valor))

valor = 34
print(valor)
print(type(valor))

valor = 3.1416
print(valor)
print(type(valor))

valor = False 
print(valor)
print(type(valor))
