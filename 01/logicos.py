# and or not nos permiten comparar valores booleanos dando un resultado booleanos

resultado = True and True
print(resultado)
resultado = True and False
print(resultado)
resultado = False and False
print(resultado)
resultado = True or True
print(resultado)
resultado = True or False
print(resultado)
resultado = False or False
print(resultado)
resultado = not True
print(resultado)
resultado = not False
print(resultado)
