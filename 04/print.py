#en python podemos imprimir una N cantidad de valores con la funcion print

nombre = "Tony"
apellido = "Stark"

print(nombre, apellido, "Ironman") #utilizamos coma para separar los valores

#la funcion print por defecto separa los valores con un espacio

print(nombre, apellido, "Ironman", sep="|") #utilizamos el parametro sep para cambiar el separador
